export async function updatePartnerStatus({
  id,
  status,
}: {
  id: string;
  status: PartnerStatus;
}) {
  const {
    data: { user },
  } = await typedSupabase.auth.getUser();
  if (!user) throw new Error('Not authenticated');

  // Fetch current row to resolve role and validate transition
  const { data: row, error: fetchError } = await typedSupabase
    .from('gym_partners')
    .select('requester_id, status')
    .eq('id', id)
    .single();

  if (fetchError) {
    captureException(fetchError);
    throw fetchError;
  }

  const role = row.requester_id === user.id ? 'requester' : 'responder';
  const currentStatus = row.status as PartnerStatus;

  if (!canTransition({ currentStatus, targetStatus: status, role })) {
    throw new Error(
      `Invalid transition: ${currentStatus} → ${status} as ${role}`,
    );
  }

  const { error } = await typedSupabase
    .from('gym_partners')
    .update({ status })
    .eq('id', id);

  if (error) {
    captureException(error);
    throw error;
  }
}

